/**Để vượt qua thử thách này, bạn hãy hoàn thành hàmgetContentLength.
 Hàm này sẽ trả về độ dài của tham số content. */

 function getContentLength(content) {
    return content.length;
}


// Mở tab Console để xem kết quả trực quan
console.log(getContentLength('JavaScript'));   // Output: 10
console.log(getContentLength('Hello World'));  // Output: 11
