// Sử dụng kiến thức về toán tử gán và toán tử số học để tạo các biến a, b, c, d thỏa mãn các yêu cầu sau đây:

// a là 1 số dương
// b là 1 số âm
// c là kết quả của a + b
// d là kết quả của a - b

var a = 5; // a là 1 số dương
var b = -3; // b là 1 số âm
var c = a + b; // c là kết quả của a + b
var d = a - b; // d là kết quả của a - b
console.log(c); // 2
console.log(d); // 8